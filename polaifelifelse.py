# pola pengkodean if elif else
nilai = 8
# percabangan dengan kondisi nilai lebih besar deari 9
if (nilai > 9):
    print("A")
# tetapi jika nilai lebih besar dari 7 akan dijalankan ASAL PERNYATAAN DI ATAS BERNILAI SALAH
elif (nilai > 7):
    print("B")
# tetapi jika nilai lebih besar dari 5 akan dijalankan ASAL PERNYATAAN DI ATAS BERNILAI SALAH
elif (nilai > 5):
    print("C")
# tetapi jika nilai lebih besar dari 3 akan dijalankan ASAL PERNYATAAN DI ATAS BERNILAI SALAH
elif (nilai > 3):
    print("D")
# jika semua kondisi salah maka E akan dicetak
else:
    print("E")
# note: apa bedanya IF dan ELIF