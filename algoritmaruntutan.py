# luas persegi panjang
# input
panjang = int(input("masukkan panjang persegi panjang : "))
lebar = int(input("masukkan lebar persegi panjang : "))

# proses
luas = panjang * lebar

# output dengan bantuan f string
print(f"luas persegi panjang dengan ukuran panjang = {panjang} dan lebar = {lebar} adalah {luas} ")