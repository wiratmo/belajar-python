nama = input("nama anda : ")
usia = input("usia anda dalam satuan tahun : ")

# mencetak hanya kata saja
print("selamat datang")

#mencetak dengan variabel
print("nama saya", nama, "usia saya", usia)

# mencetak dengan bantuan format
print("nama saya {} usia saya {} tahun".format(nama, usia))

# mencetak dengan f string
print(f"nama saya {nama} usia saya {usia} tahun")